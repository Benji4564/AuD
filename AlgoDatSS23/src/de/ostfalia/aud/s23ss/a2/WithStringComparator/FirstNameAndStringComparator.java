package de.ostfalia.aud.s23ss.a2.WithStringComparator;

import java.util.Comparator;

import de.ostfalia.aud.s23ss.base.IEmployee;

public class FirstNameAndStringComparator implements Comparator<Object>  {
    
        @Override
        public int compare(Object o1, Object o2) {
            IEmployee e1 = (IEmployee) o1;
            String s2 = (String) o2;
            return e1.getFirstName().compareTo(s2);
        }
    
}
